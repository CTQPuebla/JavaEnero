package com;

public class Prueba7 {

	public static void main(String[] args) {
		
		int a = 10, b = 0, c = 0;
		
		System.out.println("Valor de a: "+a);
		System.out.println("Valor de b: "+b);
		System.out.println("Valor de c: "+c);
		b = ++a;
		System.out.println("\nb = ++a");
		System.out.println("Valor de a: "+a);
		System.out.println("Valor de b: "+b);
		System.out.println("Valor de c: "+c);
		c = a++;
		System.out.println("\nc = a++");
		System.out.println("Valor de a: "+a);
		System.out.println("Valor de b: "+b);
		System.out.println("Valor de c: "+c);
		
	}
	
}
